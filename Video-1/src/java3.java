

public class java3 {
    public static void main(String[] args) {
        pattern1();
        pattern2();
        pattern3();

    }
    static void pattern1()
    {
        System.out.println("*\n* *\n* * *\n* * * *\n* * * * * *");
    }
    static void pattern2()
    {
        System.out.println();
        System.out.println("* * * * * *\n* * * *\n* * *\n* *\n*");
    }
    static void pattern3()
    {
        System.out.println("\t\t\t\t*");
        System.out.println("\t\t\t*\t*");
        System.out.println("\t\t*\t*\t*");
        System.out.println("\t*\t*\t*\t*");
        System.out.println("*\t*\t*\t*\t*");
    }


}
